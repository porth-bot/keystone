#!/usr/bin/env python3
"""
evaluate.py -- Held-out next-answer prediction on the synthetic cohort.

Keystone Layer-1 validation.

Protocol
--------
1. Regenerate the SAME synthetic cohort as fit_bkt.py (same seed), so the
   evaluation is deterministic and reproducible.
2. Load the fitted BKT parameters from parameters_fit.json. If that file is
   absent, fit them on the fly (calling fit_bkt).
3. For each (student, skill) sequence, HOLD OUT the final attempt. Use only the
   preceding attempts to form a prediction of P(final answer correct) with:
       (a) BKT             -- roll the belief forward with fitted {L0,T,G,S}
       (b) majority        -- constant global base rate
       (c) previous_answer -- the student's last observed result on the skill
4. Score each predictor against the true held-out labels with:
       - ROC AUC  (Mann-Whitney rank statistic, average ranks for ties)
       - Accuracy (threshold 0.5)
       - Brier score (mean squared error of the probability)
5. Write everything to results.json and print a table.

** The cohort is SYNTHETIC. These numbers describe the model's behaviour on
   data generated by the BKT process, NOT on real students. **

Standard library only. Deterministic (seeded).
"""

import json
import os
import sys

import fit_bkt
from compare_baselines import (
    compute_base_rate,
    majority_predict,
    previous_answer_predict,
)


# --------------------------------------------------------------------------- #
# Metrics (pure stdlib).
# --------------------------------------------------------------------------- #
def _average_ranks(values):
    """
    Return the average ranks (1-based) of `values`, assigning tied values the
    mean of the ranks they span. Used for the tie-corrected AUC.
    """
    order = sorted(range(len(values)), key=lambda i: values[i])
    ranks = [0.0] * len(values)
    i = 0
    n = len(values)
    while i < n:
        j = i
        while j + 1 < n and values[order[j + 1]] == values[order[i]]:
            j += 1
        # positions i..j (0-based) share the same value -> ranks (i+1)..(j+1)
        avg = (i + 1 + j + 1) / 2.0
        for k in range(i, j + 1):
            ranks[order[k]] = avg
        i = j + 1
    return ranks


def roc_auc(scores, labels):
    """
    ROC AUC via the Mann-Whitney U statistic with average ranks for ties.

    AUC = (sum_of_ranks_of_positives - n_pos*(n_pos+1)/2) / (n_pos * n_neg)

    Returns None if only one class is present (AUC undefined).
    """
    n = len(labels)
    n_pos = sum(1 for y in labels if y == 1)
    n_neg = n - n_pos
    if n_pos == 0 or n_neg == 0:
        return None
    ranks = _average_ranks(scores)
    sum_pos = sum(ranks[i] for i in range(n) if labels[i] == 1)
    return (sum_pos - n_pos * (n_pos + 1) / 2.0) / (n_pos * n_neg)


def accuracy(scores, labels, threshold=0.5):
    """Fraction correct when a prediction >= threshold is called 'correct'."""
    if not labels:
        return None
    hits = 0
    for p, y in zip(scores, labels):
        pred = 1 if p >= threshold else 0
        if pred == y:
            hits += 1
    return hits / len(labels)


def brier_score(scores, labels):
    """Mean squared error between predicted probability and 0/1 outcome."""
    if not labels:
        return None
    return sum((p - y) ** 2 for p, y in zip(scores, labels)) / len(labels)


# --------------------------------------------------------------------------- #
# BKT rolled-forward prediction over a history.
# --------------------------------------------------------------------------- #
def bkt_predict_next(history, params):
    """
    Given prior 0/1 responses `history` on a skill and fitted params, roll the
    BKT belief forward and return P(next answer correct).
    """
    L = params["L0"]
    T, G, S = params["T"], params["G"], params["S"]
    for obs in history:
        L = fit_bkt.bkt_update(L, obs == 1, T, G, S)
    return fit_bkt.bkt_predict(L, G, S)


# --------------------------------------------------------------------------- #
# Load or produce fitted parameters.
# --------------------------------------------------------------------------- #
def load_or_fit_params(here, seed):
    """Load parameters_fit.json if present; otherwise fit and return params."""
    path = os.path.join(here, "parameters_fit.json")
    if os.path.exists(path):
        with open(path, encoding="utf-8") as fh:
            payload = json.load(fh)
        if payload.get("dataset") == "synthetic_cohort" and "skills" in payload:
            print("Loaded fitted parameters from %s" % path)
            return payload["skills"]
        else:
            print("parameters_fit.json is not a synthetic fit; refitting.")
    print("No synthetic parameters_fit.json found; fitting now...")
    cohort = fit_bkt.generate_cohort(seed=seed)
    by_skill = fit_bkt.cohort_to_sequences(cohort)
    fitted, _ = fit_bkt.fit_all_skills(by_skill, seed=seed)
    return fitted


# --------------------------------------------------------------------------- #
# Main evaluation.
# --------------------------------------------------------------------------- #
def main(argv=None):
    here = os.path.dirname(os.path.abspath(__file__))
    seed = fit_bkt.DEFAULT_SEED

    print("Keystone BKT evaluation -- SYNTHETIC cohort (NOT real students)")
    params = load_or_fit_params(here, seed)

    # Regenerate the identical cohort used for fitting.
    cohort = fit_bkt.generate_cohort(seed=seed)

    # Build training sequences (everything except each held-out last attempt)
    # so the base rate for the baselines never peeks at held-out labels.
    train_sequences = []
    for student in cohort:
        for skill, seq in student.items():
            if len(seq) >= 2:
                train_sequences.append(seq[:-1])
    base_rate = compute_base_rate(train_sequences)

    # Collect held-out predictions.
    bkt_scores, maj_scores, prev_scores, labels = [], [], [], []
    for student in cohort:
        for skill, seq in student.items():
            if len(seq) < 2:
                continue  # need at least one history point + one held-out point
            history = seq[:-1]
            label = seq[-1]

            bkt_scores.append(bkt_predict_next(history, params[skill]))
            maj_scores.append(majority_predict(base_rate))
            prev_scores.append(previous_answer_predict(history, base_rate))
            labels.append(label)

    n = len(labels)
    pos = sum(labels)
    print("\nHeld-out predictions: %d (positives=%d, base_rate=%.4f)"
          % (n, pos, base_rate))

    models = {
        "bkt": bkt_scores,
        "majority": maj_scores,
        "previous_answer": prev_scores,
    }

    metrics = {}
    for name, scores in models.items():
        metrics[name] = {
            "auc": roc_auc(scores, labels),
            "accuracy": accuracy(scores, labels),
            "brier": brier_score(scores, labels),
        }

    # ---- pretty table ----
    print()
    print("Results (synthetic held-out next-answer prediction)")
    print("-" * 60)
    print("%-18s %8s %10s %10s" % ("model", "AUC", "accuracy", "Brier"))
    print("-" * 60)
    for name in ("bkt", "majority", "previous_answer"):
        m = metrics[name]
        auc = "n/a" if m["auc"] is None else "%.4f" % m["auc"]
        print("%-18s %8s %10.4f %10.4f"
              % (name, auc, m["accuracy"], m["brier"]))
    print("-" * 60)
    print("Higher AUC/accuracy is better; lower Brier is better.")

    # ---- write results.json ----
    payload = {
        "dataset": "synthetic_cohort",
        "note": ("SYNTHETIC data. This evaluation uses a cohort generated by "
                 "the BKT generative process, NOT real students. Numbers are "
                 "the actual computed metrics on that synthetic held-out set."),
        "protocol": ("Per student-skill sequence, hold out the final attempt "
                     "and predict it from the preceding attempts."),
        "seed": seed,
        "n_heldout": n,
        "n_positive": pos,
        "base_rate": base_rate,
        "metrics_definition": {
            "auc": "ROC AUC via Mann-Whitney rank statistic (tie-corrected).",
            "accuracy": "Threshold 0.5.",
            "brier": "Mean squared error of predicted probability.",
        },
        "models": metrics,
    }
    out_path = os.path.join(here, "results.json")
    with open(out_path, "w", encoding="utf-8") as fh:
        json.dump(payload, fh, indent=2, sort_keys=True)
    print("\nResults written to %s" % out_path)
    return 0


if __name__ == "__main__":
    sys.exit(main())
